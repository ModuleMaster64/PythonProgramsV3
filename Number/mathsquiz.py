# 🦛 Hippo Problem
print("If a 2000 pound pregnant hippo gives birth to a 100 pound calf,")
print("but then eats 50 pounds of food, how much does she weigh?")
input("Press the enter key to find out.")
print("2000 - 100 + 50 =", 2000 - 100 + 50)
print()

# ⏳ Time Conversion question
print("If an adventurer returns from a successful quest and rests for 3 hours,")
print("how much time is that in minutes?")
input("Press the enter key to find out.")
hours = 3
minutes = hours * 60
print(f"{hours} hours is equal to {minutes} minutes.")
print()

# 💰 Splitting the Bill question
print("Your friends split £19 evenly 4 ways, how much do you each throw in?")
